#include "3-calc.h"

/**
 *op_add - adds two num
 *@a: first num
 *@b: senc num
 *Return: sum of
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - substract two num
 *@a: first num
 *@b: senc num
 *Return: subtract of
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - muls two num
 *@a: first num
 *@b: senc num
 *Return: mul of
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - div two num
 *@a: first num
 *@b: senc num
 *Return: div of
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - mod two num
 *@a: first num
 *@b: senc num
 *Return: mod of
 */

int op_mod(int a, int b)
{
	return (a % b);
}
